/*
    
    == eşit mi
    != : Eşit değil mi
    > : büyük mü
    < : küçük mü
    >= : büyük eşit mi
    <= : küçük eşit mi

*/

console.log(5==3); //consolda false çıkar
console.log(5!=3); // consolda true çıkar

let yas =23;
console.log(yas!=23); //consolda false çıkar
console.log(yas!=18); // consolda true çıkar

console.log(15>12) // consolda true çıkar
console.log(15<12) // consolda false çıkar
console.log(12<12)  // consolda false çıkar
console.log(12<=12)  // consolda true çıkar  12<12 || 12=12 olarak yorumlanır
console.log(12>=12)  // consolda true çıkar  12>12 || 12=12 olarak yorumlanır
